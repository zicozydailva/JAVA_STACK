public class loop {
    public static void main(String[] args) {
//        FOR LOOP
        for (int i = 1; i <= 10 ; i++) {
            System.out.println(i);
        }

//        WHILE LOOP
        int j = 1;
        while (j <= 5) {
            System.out.println(j);
            j++;
        }
    }
}
